#include "tests.h"

void test_calculator() {
	std::cout << "Testing the calculator function...\n";
	assert(calculate_single("1+2") == 3);
	assert(calculate_single("5 -2 * 2") == 1);
	assert(calculate_single("5 -2 * 2") == 1);
	assert(calculate_single("12 / -2") == -6);
	assert(calculate_single("5.2 / 1.25") == 4.16);
	assert(calculate_single("2^4 * 5") == 80);
	assert(calculate_single("2^(4*5)") == 1048576);
	assert(calculate_single("sin(0) + 2.5") == 2.5);
	assert(calculate_single("2.5 - tan(0)") == 2.5);
	assert(calculate_single("100 * 15 + 7") == 1507);
	assert(calculate_single("10 * ( 5 - 2 ) + 9") == 39);
	std::cout << "Calculator tests successfully completed!\n";
}

int main() {
	test_calculator();
	return 0;
}
